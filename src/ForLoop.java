//Simple example for introducing FOR loop.

public class ForLoop {
    public static void main(String[] args){
        System.out.println("Increasing order");
        for(int i=0; i<=9; i++){
            System.out.print(i+" ");

        }
        System.out.println('\n');
        System.out.println("Decreasing order");
        for(int j= 9; j>=0; j--){
            System.out.print(j+" ");
        }
    }
}
